					<h3>Argumento</h3>
					<hr>
					<p class="p_dossier">En el año 2029, después de devastar la Tierra y esclavizar a la humanidad, las máquinas, gobernadas por la inteligencia artificial conocida como Skynet, están a punto de perder la guerra contra la resistencia humana liderada por John Connor. Frente a esa situación, las máquinas entienden que asesinar a John Connor en el presente, sería irrelevante, dado que ya ha conducido a la resistencia humana del mundo entero a la victoria. Por lo tanto, Skynet elabora su estrategia decidiendo eliminar al líder enemigo antes de que éste nazca, de modo que no pueda cumplirse su misión de conducción futura. Para ello envía al pasado (al año 1984) a un Terminator T-800 modelo Cyberdyne 101, un cíborg asesino (Arnold Schwarzenegger), a través de una máquina del tiempo, con la misión de exterminar a Sarah Connor (Linda Hamilton), madre de John, antes de que éste sea concebido.</p>

