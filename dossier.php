					<p class="p_dossier">Enterados del plan para asesinar a la mujer que dará a luz al único hombre capaz de salvar a la humanidad, la resistencia también consigue acceder a la máquina del tiempo y logra enviar a un soldado humano, Kyle Reese (Michael Biehn), con la misión de protegerla del T-800.</p>

					<p class="p_dossier">Cuando el cyborg llega a 1984, sistemáticamente localiza y asesina a dos mujeres llamadas Sarah Connor, de quienes encontró su dirección en la guía telefónica de Los Ángeles. La policía está sorprendida por la coincidencia de que los asesinatos ocurrieron el mismo día, por el nombre similar de las víctimas, y porque no tenían ninguna relación entre ellas. Cuando Sarah J. Connor salía de su turno de trabajo como camarera de un restaurante, su compañera de trabajo le muestra un noticiero de televisión donde anuncian que una mujer llamada Sarah Connor había sido asesinada esa misma mañana.</p>


