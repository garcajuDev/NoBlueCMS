					<p class="p_dossier">The Terminator (distribuida en el mundo hispanohablante como Terminator o El Exterminador) es una película estadounidense de ciencia ficción y acción de 1984, dirigida por James Cameron, coescrita entre Cameron, Gale Anne Hurd y William Wisher Jr. y protagonizada por Arnold Schwarzenegger, Linda Hamilton y Michael Biehn. El filme, rodado en Los Ángeles, fue producido por Hemdale Film Corporation y distribuido por Orion Pictures. Schwarzenegger interpreta al Terminator, un ciborg asesino enviado a través del tiempo desde el año 2029 a 1984 para asesinar a Sarah Connor, interpretada por Linda Hamilton. Biehn es Kyle Reese, un soldado también enviado desde el futuro con la misión de proteger a Sarah. Fue filmada de marzo a mayo de 1984 y finalmente estrenada el 20 de octubre de 1984.</p>

					<p class="p_dossier">Aunque no se esperaba que fuera un éxito comercial o de crítica, Terminator encabezó la taquilla estadounidense durante dos semanas y ayudó a lanzar la carrera cinematográfica de James Cameron y consolidar la de Arnold Schwarzenegger. En el caso de la guionista y productora Gale Anne Hurd, el film implicó un salto en su carrera como productora, pero fue el primer y único guion que escribió.3​ Se han rodado cuatro secuelas: Terminator 2: el juicio final (1991), Terminator 3: la rebelión de las máquinas (2003) Terminator Salvation (2009); y Terminator: Génesis (2015). En 2008, la película fue considerada «cultural, histórica y estéticamente significativa» por la Biblioteca del Congreso de Estados Unidos y seleccionada para su preservación en el National Film Registry.4​</p>

